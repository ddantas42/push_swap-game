#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t		n;

	if (s && f)
	{
		n = 0;
		while (n < ft_strlen(s))
		{
			f(n, &s[n]);
			n++;
		}
		s[n] = 0;
	}
}
