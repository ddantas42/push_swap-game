#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int	n;

	if (s && fd)
	{
		n = 0;
		while (s[n])
		{
			write(fd, &s[n], 1);
			n++;
		}
		write(fd, "\n", 1);
	}
}
