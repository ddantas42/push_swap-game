#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	a;
	int		c;

	c = 1;
	while (fd && c--)
	{
		if (n == -2147483648)
		{
			write(fd, "-2", 2);
			ft_putnbr_fd(147483648, fd);
			break ;
		}
		if (n < 0)
		{
			n *= -1;
			write(fd, "-", 1);
		}
		if (n > 9)
			ft_putnbr_fd(n / 10, fd);
		a = 48 + n % 10;
		if (a <= '9' && a >= '0')
			write(fd, &a, 1);
	}
}
